package main

import "fmt"

func main() {
	var num1, num2 int

	fmt.Println("Digite o primeiro número:")
	fmt.Scanln(&num1)
	fmt.Println("Digite o segundo número:")
	fmt.Scanln(&num2)

	if num1 > num2 {
		fmt.Println("O maior número é:", num1)
	} else if num2 > num1 {
		fmt.Println("O maior número é:", num2)
	} else {
		fmt.Println("Os números são iguais.")
	}
}
